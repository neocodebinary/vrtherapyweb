function changeLanguage() {
    const lang = document.getElementById('languageSelector').value;
    const texts = translations[lang] || translations['en'];

    const elements = [
        'heroTitle', 'heroDesc',
        'txtStoreGoogle', 'txtStoreApple',
        'feat1Title', 'feat1Desc',
        'feat2Title', 'feat2Desc',
        'feat3Title', 'feat3Desc',
        'supportTitle', 'supportDesc', 'btnSupport',
        'linkPrivacy', 'linkTerms', 'txtCopyright'
    ];

    elements.forEach(id => {
        const el = document.getElementById(id);
        if (el && texts[id]) {
            el.innerText = texts[id];
        }
    });
}

// --- 3D Tilt Effect (Subtle) ---
document.addEventListener('mousemove', (e) => {
    const cards = document.querySelectorAll('.tilt-card');

    cards.forEach(card => {
        const rect = card.getBoundingClientRect();
        if (rect.bottom < 0 || rect.top > window.innerHeight) return;

        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;
        const centerX = rect.width / 2;
        const centerY = rect.height / 2;
        const rotateX = ((y - centerY) / centerY) * -2;
        const rotateY = ((x - centerX) / centerX) * 2;

        card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.005, 1.005, 1.005)`;
    });
});

document.addEventListener('mouseleave', () => {
    const cards = document.querySelectorAll('.tilt-card');
    cards.forEach(card => {
        card.style.transform = 'perspective(1000px) rotateX(0) rotateY(0) scale3d(1, 1, 1)';
    });
});